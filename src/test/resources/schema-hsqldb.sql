create table quote_author (
  id            bigint          generated by default as identity(start with 1) not null primary key,
  user_id       varchar(128)    not null,
  author        varchar(64)     not null,
  description   varchar(1024)   default '',
  created       timestamp       default current_timestamp not null,
  updated       timestamp       default current_timestamp not null
);

alter table quote_author add foreign key (user_id) references user (user_id)
  on delete cascade;

create unique index idx_quote_user_authors on quote_author (user_id, author);

create table quote_tag (
  id              bigint          generated by default as identity(start with 1) not null primary key,
  user_id         varchar(128)    not null,
  tag             varchar(64)     not null,
  created         timestamp       default current_timestamp not null
);

create unique index idx_quote_user_tags on quote_tag (user_id, tag);

create table quote (
  id              bigint          generated by default as identity(start with 1) not null primary key,
  user_id         varchar(128)    not null,
  quote           varchar(1024)   default '' not null,
  created         timestamp       default current_timestamp not null,
  updated         timestamp       default current_timestamp not null,
  quote_author_id bigint          default null,
  quote_tag_id    bigint          default null
);

alter table quote add foreign key (user_id) references user (user_id)

alter table quote add foreign key (quote_author_id) references quote_author (id)
  on delete set null;

alter table quote add foreign key (quote_tag_id) references quote_tag (id)
  on delete set null;

